package Abstraction;

public class SampleLogic extends Sample
{

	@Override
	public int add(int a, int b) {
		
		int c=a+b;
		
		return c;
		
	}

	@Override
	public int mul(int a, int b) {
		
		int c=a*b;
		
		return c;
		
	}

	@Override
	public float add(float a, float b) {
		
		float c=a+b;
		
		return c;
	}

	
	

}
